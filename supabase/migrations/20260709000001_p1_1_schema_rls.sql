-- hospitals -------------------------------------------------------------------
-- Doctors read hospitals they are a member of. Patients read branding via the
-- hospital_public view (granted below). Edge fn = service role (bypasses RLS).
create policy hospitals_doctor_read on public.hospitals
  for select to authenticated
  using (public.is_member(id));

-- profiles --------------------------------------------------------------------
-- Own row read/write (first-run wizard writes here).
create policy profiles_self_select on public.profiles
  for select to authenticated using (id = auth.uid());
create policy profiles_self_insert on public.profiles
  for insert to authenticated with check (id = auth.uid());
create policy profiles_self_update on public.profiles
  for update to authenticated using (id = auth.uid()) with check (id = auth.uid());

-- memberships -----------------------------------------------------------------
-- Staff read their own memberships. Writes are operator-provisioned (service
-- role only) — no authenticated insert/update/delete policy.
create policy memberships_self_select on public.memberships
  for select to authenticated using (profile_id = auth.uid());

-- patient_details -------------------------------------------------------------
-- Patient owns their row (r/w). Doctor reads details of patients who have a
-- consult in the doctor's hospital.
create policy pd_owner_select on public.patient_details
  for select to authenticated using (profile_id = auth.uid());
create policy pd_owner_insert on public.patient_details
  for insert to authenticated with check (profile_id = auth.uid());
create policy pd_owner_update on public.patient_details
  for update to authenticated using (profile_id = auth.uid()) with check (profile_id = auth.uid());
create policy pd_doctor_select on public.patient_details
  for select to authenticated
  using (exists (
    select 1 from public.consults c
    where c.patient_id = patient_details.profile_id and public.is_member(c.hospital_id)
  ));

-- consults --------------------------------------------------------------------
-- Patient reads own consults and inserts own (into their chosen hospital).
-- Doctor r/w consults where hospital_id ∈ their memberships.
create policy consults_patient_select on public.consults
  for select to authenticated using (patient_id = auth.uid());
create policy consults_patient_insert on public.consults
  for insert to authenticated with check (patient_id = auth.uid());
create policy consults_doctor_select on public.consults
  for select to authenticated using (public.is_member(hospital_id));
create policy consults_doctor_update on public.consults
  for update to authenticated
  using (public.is_member(hospital_id)) with check (public.is_member(hospital_id));

-- consult_messages ------------------------------------------------------------
-- Patient reads messages of own consults and may INSERT PATIENT TURNS ONLY.
-- Doctor reads messages for consults in their hospital.
create policy cm_patient_select on public.consult_messages
  for select to authenticated
  using (exists (
    select 1 from public.consults c
    where c.id = consult_messages.consult_id and c.patient_id = auth.uid()
  ));
create policy cm_patient_insert on public.consult_messages
  for insert to authenticated
  with check (
    sender = 'patient'
    and exists (
      select 1 from public.consults c
      where c.id = consult_messages.consult_id and c.patient_id = auth.uid()
    )
  );
create policy cm_doctor_select on public.consult_messages
  for select to authenticated
  using (exists (
    select 1 from public.consults c
    where c.id = consult_messages.consult_id and public.is_member(c.hospital_id)
  ));

-- review_messages -------------------------------------------------------------
-- Doctor-only, scoped to their hospital (patients never see these).
create policy rm_doctor_select on public.review_messages
  for select to authenticated
  using (exists (
    select 1 from public.consults c
    where c.id = review_messages.consult_id and public.is_member(c.hospital_id)
  ));
create policy rm_doctor_insert on public.review_messages
  for insert to authenticated
  with check (
    doctor_id = auth.uid()
    and exists (
      select 1 from public.consults c
      where c.id = review_messages.consult_id and public.is_member(c.hospital_id)
    )
  );

