-- =============================================================================
-- RLS POLICIES — additive grants per ARCHITECTURE §8.3. Anything not granted
-- here is denied (RLS is enabled on every table above). The service_role used
-- by edge functions bypasses RLS entirely ("all" column of the matrix).
