-- Consult status enum (ARCHITECTURE §8.2 / §8.4 state machine) -----------------
-- Transitions are enforced by a DB trigger in P1.2; here we only define the type.
create type public.consult_status as enum (
  'active',
  'pending_review',
  'approved',
  'rejected',
  'escalated',
  'communicated',
  'closed',
  'superseded',
  'abandoned',
  'expired'
);

-- =============================================================================
-- Tables (PRD §6.4). All PKs uuid default gen_random_uuid(); all timestamps
-- timestamptz default now() (ARCHITECTURE §8.2). RLS enabled default-deny
-- immediately after each CREATE.
-- =============================================================================

-- 1. hospitals — the tenant (ARCHITECTURE §9). Per-hospital config = theme +
--    ai_config only.
create table public.hospitals (
  id         uuid primary key default gen_random_uuid(),
  slug       text unique not null,
  name       text not null,
  logo_url   text,
  theme      jsonb not null default '{}'::jsonb,
  ai_config  jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);
alter table public.hospitals enable row level security;

-- 2. profiles — one row per auth user; id == auth.users.id.
create table public.profiles (
  id         uuid primary key references auth.users(id) on delete cascade,
  full_name  text,
  role       text not null default 'patient' check (role in ('patient','doctor','admin')),
  created_at timestamptz not null default now()
);
alter table public.profiles enable row level security;

-- 3. memberships — staff ↔ hospital (operator-provisioned; no self-signup).
create table public.memberships (
  id          uuid primary key default gen_random_uuid(),
  profile_id  uuid not null references public.profiles(id) on delete cascade,
  hospital_id uuid not null references public.hospitals(id) on delete cascade,
  role        text not null default 'doctor' check (role in ('doctor','admin')),
  created_at  timestamptz not null default now(),
  unique (profile_id, hospital_id)
);
alter table public.memberships enable row level security;

-- 4. patient_details — profile-scoped medical record (profile_id is the PK).
create table public.patient_details (
  profile_id  uuid primary key references public.profiles(id) on delete cascade,
  dob         date,
  sex         text,
  blood_group text,
  allergies   jsonb not null default '[]'::jsonb,
  conditions  jsonb not null default '[]'::jsonb,
  medications jsonb not null default '[]'::jsonb,
  updated_at  timestamptz not null default now()
);
alter table public.patient_details enable row level security;

-- 5. consults — the unit of work. status enum incl. abandoned/expired.
create table public.consults (
  id             uuid primary key default gen_random_uuid(),
  hospital_id    uuid not null references public.hospitals(id) on delete cascade,
  patient_id     uuid not null references public.profiles(id) on delete cascade,
  status         public.consult_status not null default 'active',
  chief_complaint text,
  urgency        text,
  ai_confidence  numeric,
  ai_flags       jsonb not null default '[]'::jsonb,
  created_at     timestamptz not null default now(),
  submitted_at   timestamptz
);
alter table public.consults enable row level security;

-- 6. consult_messages — patient ↔ AI intake turns.
create table public.consult_messages (
  id         uuid primary key default gen_random_uuid(),
  consult_id uuid not null references public.consults(id) on delete cascade,
  sender     text not null check (sender in ('patient','ai')),
  content    text,
  ai_note    text,
  created_at timestamptz not null default now()
);
alter table public.consult_messages enable row level security;

-- 7. review_messages — doctor ↔ AI during review (never seen by patient).
create table public.review_messages (
  id         uuid primary key default gen_random_uuid(),
  consult_id uuid not null references public.consults(id) on delete cascade,
  doctor_id  uuid not null references public.profiles(id) on delete cascade,
  sender     text not null check (sender in ('doctor','ai')),
  content    text,
  citations  jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now()
);
alter table public.review_messages enable row level security;

-- 8. ai_drafts — AI recommendation payloads (written only by the edge fn).
create table public.ai_drafts (
  id             uuid primary key default gen_random_uuid(),
  consult_id     uuid not null references public.consults(id) on delete cascade,
  raw_response   jsonb,
  recommendation jsonb,
  created_at     timestamptz not null default now()
);
alter table public.ai_drafts enable row level security;

-- 9. prescriptions — approved output. supersedes_id self-FK (§8.2); a superseding
--    insert flips the prior consult to 'superseded' via the approval RPC (P2+).
create table public.prescriptions (
  id                uuid primary key default gen_random_uuid(),
  consult_id        uuid not null references public.consults(id) on delete cascade,
  hospital_id       uuid not null references public.hospitals(id) on delete cascade,
  patient_id        uuid not null references public.profiles(id) on delete cascade,
  doctor_id         uuid not null references public.profiles(id) on delete cascade,
  items             jsonb not null default '[]'::jsonb,
  advice            text,
  edited_from_draft boolean not null default false,
  supersedes_id     uuid references public.prescriptions(id) on delete set null,
  approved_at       timestamptz not null default now()
);
alter table public.prescriptions enable row level security;

-- 10. reviews — the doctor's decision record for a consult.
create table public.reviews (
  id         uuid primary key default gen_random_uuid(),
  consult_id uuid not null references public.consults(id) on delete cascade,
  doctor_id  uuid not null references public.profiles(id) on delete cascade,
  action     text not null check (action in ('approved','edited','rejected','escalated')),
  reason     text,
  diff       jsonb,
  created_at timestamptz not null default now()
);
alter table public.reviews enable row level security;

-- 11. mira_feedback — doctor corrections that tune the agent (per hospital).
create table public.mira_feedback (
  id         uuid primary key default gen_random_uuid(),
  hospital_id uuid not null references public.hospitals(id) on delete cascade,
  doctor_id  uuid not null references public.profiles(id) on delete cascade,
  consult_id uuid references public.consults(id) on delete set null,
  feedback   text,
  category   text not null check (category in ('correction','style','protocol')),
  created_at timestamptz not null default now()
);
alter table public.mira_feedback enable row level security;

-- 12. consult_media — patient-uploaded images/video for a consult.
create table public.consult_media (
  id           uuid primary key default gen_random_uuid(),
  consult_id   uuid not null references public.consults(id) on delete cascade,
  kind         text not null check (kind in ('image','video')),
  storage_path text not null,
  ai_findings  jsonb,
  created_at   timestamptz not null default now()
);
alter table public.consult_media enable row level security;

-- 13. consult_events — append-only lifecycle log (ARCHITECTURE §8.2/§8.5).
--     Only the service role / SQL triggers write here; append-only for ALL
--     (enforced by a no-mutate trigger below, not merely by absent grants).
create table public.consult_events (
  id          uuid primary key default gen_random_uuid(),
  consult_id  uuid not null references public.consults(id) on delete cascade,
  hospital_id uuid not null references public.hospitals(id) on delete cascade,
  event_type  text not null check (event_type in
                ('status_change','queued','doctor_opened','notification_sent',
                 'sla_escalated','expired','system')),
  actor       text not null check (actor in ('patient','ai','doctor','system')),
  actor_id    uuid,
  payload     jsonb not null default '{}'::jsonb,
  created_at  timestamptz not null default now()
);
alter table public.consult_events enable row level security;

-- Append-only enforcement: reject UPDATE/DELETE from anyone, incl. service role.
create or replace function public.consult_events_append_only()
returns trigger
language plpgsql
as $$
begin
  raise exception 'consult_events is append-only (% not permitted)', tg_op;
end;
$$;

create trigger consult_events_no_mutate
  before update or delete on public.consult_events
  for each row execute function public.consult_events_append_only();

-- =============================================================================
-- Tenancy helper functions (SECURITY DEFINER: bypass RLS to avoid policy
-- recursion when a policy needs to consult memberships/profiles). Defined after
-- the tables they reference so the SQL bodies validate at creation time.
-- =============================================================================

-- Is the current user a staff member of hospital `h`?
create or replace function public.is_member(h uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.memberships m
    where m.profile_id = auth.uid() and m.hospital_id = h
  );
$$;

-- The current user's platform role ('patient' | 'doctor' | 'admin' | null).
create or replace function public.current_role_of()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select p.role from public.profiles p where p.id = auth.uid();
$$;

